#include <iostream>
using namespace std;

int main() {
    int n;
    double sum = 0;
    double temperature;

    cout << "Enter the number of temperatures: ";
    cin >> n;

    if (n <= 0) {
        cout << "The number of temperatures must be positive." << endl;
        return 1;
    }

    cout << "Enter the temperatures:" << endl;
    for (int i = 0; i < n; ++i) {
        cin >> temperature;
        sum += temperature;
    }

    cout << "The average temperature is: " << sum / n << " degrees Celsius." << endl;

    return 0;
}
